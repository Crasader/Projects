package com.dreamgear.majiang.game.rank;

public class RankGiftData {
	private long id;
	private int rank_min;
	private int rank_max;
	private int count;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public int getRank_min() {
		return rank_min;
	}
	public void setRank_min(int rank_min) {
		this.rank_min = rank_min;
	}
	public int getRank_max() {
		return rank_max;
	}
	public void setRank_max(int rank_max) {
		this.rank_max = rank_max;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
}
